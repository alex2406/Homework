public class NumDividBy5And6 {
    public static void main(String[] args) {
        for (int i = 100; i <= 1000; i++) {

            if (i % 5 == 0)
                System.out.print(i+" ");
                if (i % 6 == 0)
                    System.out.print(i+" ");


        }
    }
}
